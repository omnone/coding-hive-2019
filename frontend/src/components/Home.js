//////////////////////////////////////////////////////////////////////////////////////

export default function Dashboard(props) {
  const classes = useStyles();
  const [open, setOpen] = React.useState(true);
  const handleDrawerOpen = () => {
    setOpen(true);
  };
  const handleDrawerClose = () => {
    setOpen(false);
  };
  const fixedHeightPaper = clsx(classes.paper, classes.fixedHeight);

  //////////////////////////////////////////////////////////////////////////////////////

  //Edw fortonontai ta components pou tha deixnei to kendriko frame analoga to state
  //get the state of the main frame in order to view the proper page
  let mainFrame,subtitle;

  if (props.isCreate) {
    // *******Edw tha prepei na mpei to component tis formas gia na dimiourgisei
    // o xristis ena issue
    mainFrame = <h1>Create Issue</h1>;
    subtitle = "Δημιουργία";
  } else if (props.isSearch) {
    mainFrame = <Table />;
    subtitle = "Αναζήτηση";

  }
  // }else if (props.isUpdate) {
  //   update component here
  // }



  //////////////////////////////////////////////////////////////////////////////////////

  return (
    <div className={classes.root}>
      <CssBaseline />
      <AppBar
        position="absolute"
        className={clsx(classes.appBar, open && classes.appBarShift)}
      >
        <Toolbar className={classes.toolbar}>
          <IconButton
            edge="start"
            color="inherit"
            aria-label="open drawer"
            onClick={handleDrawerOpen}
            className={clsx(
              classes.menuButton,
              open && classes.menuButtonHidden
            )}
          >
            <MenuIcon />
          </IconButton>
          
          <Typography
            component="h1"
            variant="h6"
            color="inherit"
            noWrap
            className={classes.title}
          >
            Διαχείριση Θεμάτων - {subtitle}
          </Typography>
        </Toolbar>
      </AppBar>
      <Drawer
        variant="permanent"
        classes={{
          paper: clsx(classes.drawerPaper, !open && classes.drawerPaperClose)
        }}
        open={open}
      >
        <div className={classes.toolbarIcon}>
          <IconButton onClick={handleDrawerClose}>
            <ChevronLeftIcon />
          </IconButton>
        </div>
        {/* SideMenu */}
        <Divider />
        <SideMenu
          value={props.value}
          username={props.username}
          create={props.create}
          search={props.search}
        />
        <Divider />
      </Drawer>
      <main
        className={classes.content}
        style={{ backgroundColor: "rgba(255,255,255,0.95)" }}
      >
        <div className={classes.appBarSpacer} />
        <Container maxWidth="lg" className={classes.container}>
          <Grid container spacing={3}>
            {/* Main Table */}
            <Grid item xs={12}>
              <Paper className={classes.paper}>
                {/* render the main frame */}
                {mainFrame}
              </Paper>
            </Grid>
          </Grid>
          <Box pt={4}>
            <Copyright />
          </Box>
        </Container>
      </main>
    </div>
  );
}
